/// Defines valid run modes for the [Serverpod].
class ServerpodRunMode {
  /// The server is run in development mode on a local machine.
  static const development = 'development';

  /// The server is run in test mode.
  static const test = 'test';

  /// The server is run in staging mode.
  static const staging = 'staging';

  /// The server is run in production mode.
  static const production = 'production';
}
